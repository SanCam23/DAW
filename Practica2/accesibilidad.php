            <section>
                <h2>Estilos Accesibles Disponibles</h2>

                <h3>1. Modo Oscuro</h3>
                <p>
                    Este estilo invierte los colores del fondo y el texto, reduciendo la fatiga visual y mejorando la
                    lectura en entornos con poca luz.
                    <strong>Fondo oscuro</strong> y <strong>texto claro</strong> proporcionan un alto nivel de comodidad
                    visual.
                </p>

                <h3>2. Alto Contraste</h3>
                <p>
                    El estilo de alto contraste mejora la legibilidad al aumentar la diferencia entre el color del texto
                    y el fondo.
                    Este modo es útil para usuarios con <strong>discapacidad visual o daltonismo</strong>.
                </p>

                <h3>3. Letra Grande</h3>
                <p>
                    El estilo de letra grande incrementa el tamaño del texto y el espaciado entre líneas para facilitar
                    la lectura a personas con <strong>baja visión</strong>.
                </p>

                <h3>4. Alto Contraste con Letra Grande</h3>
                <p>
                    Este modo combina ambos ajustes para ofrecer una experiencia óptima a usuarios que necesitan
                    simultáneamente un texto más grande y mayor contraste de color.
                </p>
            </section>

            <section>
                <h2>Cómo Activar un Estilo Accesible</h2>
                <p>
                    En navegadores como <strong>Mozilla Firefox</strong>, los
                    usuarios pueden seleccionar un estilo alternativo desde el menú
                    <em>Ver → Estilo de página</em>.
                    También se puede modificar automáticamente mediante un <strong>selector de estilo</strong> dentro de
                    la propia web.
                </p>
